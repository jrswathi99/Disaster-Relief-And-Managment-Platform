    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     int selectedRow = tblPendingresources.getSelectedRow();
    if (selectedRow < 0) {
        JOptionPane.showMessageDialog(this, "Please select a request to approve.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Get the correct request ID from the selected row
    String requestId = (String) tblPendingresources.getValueAt(selectedRow, 0); // Ensure the ID column is being fetched correctly
    double totalCost = (double) tblPendingresources.getValueAt(selectedRow, 4); // Total cost column

    // Approve the request
    boolean success = financeManager.approveRequest(requestId, totalCost);
    if (success) {
        JOptionPane.showMessageDialog(this, "Request approved successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

        // Move the approved resource to available resources
        resourceManager.moveApprovedRequestToResources(requestId);

        // Update the budget details in the UI
        populateBudgetDetails();

        // Update the pending requests table
        populatePendingRequestsTable();
    } else {
        JOptionPane.showMessageDialog(this, "Insufficient budget to approve the request.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

