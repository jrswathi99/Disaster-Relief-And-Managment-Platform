    private void populatePendingRequestsTable() {
     DefaultTableModel model = (DefaultTableModel) tblPendingresources.getModel();
    model.setRowCount(0); // Clear the table

    // Iterate through all requests in the resource manager
    for (ResourceRequest request : resourceManager.getAllRequests()) {
        // Only include requests with status "Pending" and created by "LogisticsManager"
        if ("Pending".equals(request.getStatus()) && "LogisticsManager".equals(request.getRequestedBy())) {
            model.addRow(new Object[]{
                request.getId(),
                request.getRequestedBy(), // From
                request.getResourceType(),
                request.getQuantityRequested(),
                request.getPrice(),
                request.getQuantityRequested() * request.getPrice() // Total cost
            });
        }
    }
}
    private void populateBudgetDetails() {
        DecimalFormat df = new DecimalFormat("#.00");
    txtTotalBudget.setText(df.format(financeManager.getBudget().getTotalBudget()));
    txtAllocatedBudget.setText(df.format(financeManager.getBudget().getRemainingBudget())); // Remaining Budget
}

