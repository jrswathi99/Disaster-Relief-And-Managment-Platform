    private void btnrequestsecurityresourceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnrequestsecurityresourceActionPerformed
        // TODO add your handling code here:
        String resourceType = txtSecurityResouceType.getText().trim();
        int quantity;
        try {
            quantity = Integer.parseInt(txtSecurtyQuantity.getText().trim());
            if (quantity <= 0) {
                JOptionPane.showMessageDialog(this, "Quantity must be greater than 0.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid quantity. Please enter a valid number.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (resourceType.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Resource type cannot be empty.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Add the request to ResourceManager
        disasterCoordinator.requestResource(securityResourceManager, resourceType, quantity);

        // Update the Resource Requests table
        populateRequestsTable();

        // Notify the user and switch to LogisticsJPanel
        JOptionPane.showMessageDialog(this, "Resource request submitted successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnrequestsecurityresourceActionPerformed

