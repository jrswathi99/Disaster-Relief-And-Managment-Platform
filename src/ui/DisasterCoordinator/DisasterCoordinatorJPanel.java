    private void populateRequestsTable() {
        DefaultTableModel model = (DefaultTableModel) tblSecurityRequests.getModel();
        model.setRowCount(0);
        for (ResourceRequest request : securityResourceManager.getRequestsByRole(disasterCoordinator.getRoleName())) {
            model.addRow(new Object[]{request.getId(), request.getResourceType(), request.getQuantityRequested(), request.getStatus()});
        }
    }

    private void populateAvailableResourcesTable() {
        DefaultTableModel model = (DefaultTableModel) tblSecurityRequestsAvail.getModel();
        model.setRowCount(0);
        for (String resourceType : securityResourceManager.listAvailableResources().keySet()) {
            model.addRow(new Object[]{resourceType, securityResourceManager.getAssignedQuantity(resourceType)});
        }
    }
    

