        // TODO add your handling code here:
        // Create a new disaster request
        String disasterName = this.disasterName.getText().trim();
        String disasterDescription = this.disasterDescription.getText().trim();

        if (disasterName.isEmpty() || disasterDescription.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Disaster name and description cannot be empty!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

