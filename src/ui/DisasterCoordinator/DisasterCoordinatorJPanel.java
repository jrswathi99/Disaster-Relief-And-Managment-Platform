        if (!disasterCoordinator.disasterExists(disasterName)) {
            disasterCoordinator.initiateWorkRequest(String.valueOf(System.currentTimeMillis()), disasterName, disasterDescription, "Medium");
            JOptionPane.showMessageDialog(this, "Disaster created successfully!");
            disasterSet = true;
            if (this.disasterCoordinator.getDisasters() != null && !this.disasterCoordinator.getDisasters().isEmpty()) {
               // populateZoneTable(this.disasterCoordinator.getDisasters().get(0).getName());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Disaster already exists!", "Error", JOptionPane.ERROR_MESSAGE);
        }
